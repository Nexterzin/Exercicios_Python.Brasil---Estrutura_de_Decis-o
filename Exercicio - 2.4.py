# digitada é vogal ou consoante.

print('Bem vindo!!')

letra = str(input('Informe uma letra: '))

if letra == 'A':
    print('A letra é Vogal!!')
elif letra == 'E':
    print('A letra é Vogal!!')
elif letra == 'I':
    print('A letra é Vogal!!')
elif letra == 'O':
    print('A letra é Vogal!!')
elif letra == 'U':
    print('A letra é Vogal!!')
else:
    print('A letra é consoante!!')